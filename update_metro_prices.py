    for slug, city_prices in PRICES.items():
        product = db_session.query(Product).filter_by(slug=slug).first()
        if not product:
            print(f"  SKIP (product not found): {slug}")
            skipped += 1
            continue
        for city, (bmin, bmax, fmin, fmax, pmin, pmax, avg) in city_prices.items():
            pr = db_session.query(PriceRecord).filter_by(product_id=product.id, city=city).first()
            if not pr:
                pr = PriceRecord(product_id=product.id, city=city)
                db_session.add(pr)
            pr.budget_min = bmin
            pr.budget_max = bmax
            pr.fair_min = fmin
            pr.fair_max = fmax
            pr.premium_min = pmin
            pr.premium_max = pmax
            pr.avg_price = avg
            updated += 1

    db_session.commit()
    print(f"Updated {updated} price records across 7 metro cities. Skipped {skipped} products.")


